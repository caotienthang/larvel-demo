    </p>
    <p>There are two main types of cookies:</p>
    <ul>
      <li><strong>Session cookies:</strong> Temporary cookies that are erased once you close your browser. They remember your actions during a single browsing session.</li>
      <li><strong>Persistent cookies:</strong> Cookies that remain on your device for a set period (e.g., 30 days, 1 year) to remember preferences across sessions.</li>
    </ul>


    </p>

    <h2>1. Purpose of Reviews and Feedback</h2>
    <p>
      We encourage our customers to leave reviews and feedback on the products they have purchased. Reviews help other customers
      make informed decisions and allow us to improve the quality of our offerings. Your feedback is also vital for us to understand
      what we're doing right and where we can enhance our services.
    </p>

    <h2>2. How to Leave a Review</h2>
    <p>To leave a review for a product you purchased from Lunasjaya:</p>
    <ul>
      <li>Visit the product page on <strong>lunasjaya.com</strong>.</li>
      <li>Scroll to the <strong>“Reviews”</strong> section at the bottom of the page.</li>
      <li>Rate the product using the star rating system (1 to 5 stars).</li>
      <li>Write a review describing your experience with the product.</li>
      <li>Optionally, upload photos or videos (if applicable).</li>
    </ul>

    <h2>3. Guidelines for Submitting Reviews</h2>
    <p>
      We welcome all feedback, but we ask that you follow these guidelines to ensure reviews are helpful and constructive:
    </p>

    <div class="policy-note">
      <h3>Guidelines</h3>
      <ul>
        <li><strong>Honesty:</strong> Share your genuine experience.</li>
        <li><strong>Respect:</strong> Avoid offensive language, hate speech, or personal attacks.</li>
        <li><strong>Relevant Information:</strong> Include useful details (fit, quality, usage, etc.).</li>
        <li><strong>No Promotions:</strong> Do not include promotional content, URLs, or third-party links.</li>
        <li><strong>Appropriate Content:</strong> Reviews must be suitable for all audiences.</li>
      </ul>
    </div>

    <h2>4. How We Handle Reviews</h2>
    <p>
      Once your review is submitted, it may be moderated to ensure it meets our guidelines. We aim to publish all reviews that adhere
      to our policies. Reviews containing offensive language, personal attacks, or irrelevant content may be rejected or removed.
    </p>
    <ul>
      <li><strong>Approval:</strong> Once approved, your review will appear on the product page.</li>
      <li><strong>Response:</strong> Our customer service team may respond if you raised a question, concern, or suggestion.</li>
    </ul>

    <h2>5. Incentives for Leaving Reviews</h2>
    <p>
      At times, we may offer incentives (discounts or promotions) for leaving reviews. These incentives do not affect authenticity.
      All reviews should reflect the customer’s honest experience.
    </p>

    <h2>6. Addressing Negative Reviews</h2>
    <p>
      We value negative feedback because it helps us improve. If you had a negative experience, we encourage you to contact our support
      team directly so we can resolve your concerns promptly and professionally.
    </p>
    <ul>
      <li><strong>Resolution:</strong> We will work to resolve issues to your satisfaction when possible.</li>
      <li><strong>Public Response:</strong> We may respond publicly and offer solutions consistent with our Return & Refund Policy.</li>
    </ul>

    <h2>7. Verification of Reviews</h2>
    <p>
      We may request additional verification to ensure feedback is legitimate and tied to an actual purchase. This helps maintain
      the integrity of our review system.
    </p>

    <h2>8. Removing or Modifying Reviews</h2>
    <p>
      You can modify or remove your review by logging into your account and accessing your submissions. If you want help deleting
      a review, contact us at <strong>support@rabbioz.com</strong> and we will assist you.
    </p>

    <h2>9. Reviews for Third-Party Products</h2>
    <p>
      For products not sold directly by Lunasjaya but listed through third-party services, please leave feedback on the third-party
      platform. Reviews on those platforms follow their own policies.
    </p>

    <h2>10. Privacy and Confidentiality</h2>
    <p>
      When you leave a review, we may display your name and the content of your review. We do not publish personal contact details.
      If you include personal information (email/phone) in a review, we may remove it to protect your privacy.
    </p>

